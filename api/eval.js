export default async function handler(req, res) {
  if (req.method === 'OPTIONS') {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    return res.status(204).end();
  }
  if (req.method !== 'POST') {
    return res.status(405).json({ success: false, message: 'Method not allowed.' });
  }

  // Rate limiting — 5 requests per IP per minute
  const ip = req.headers['x-forwarded-for']?.split(',')[0]?.trim() || 'unknown';
  const now = Date.now();
  const entry = rateLimit.get(ip) || { count: 0, start: now };
  if (now - entry.start > 60_000) { entry.count = 0; entry.start = now; }
  entry.count++;
  rateLimit.set(ip, entry);
  if (entry.count > 5) {
    return res.status(429).json({ success: false, message: 'Too many requests — please try again in a few minutes.' });
  }

  const { initiative_name, email, website, framework_text, use_case, context, sensitivity } = req.body || {};

  // Honeypot
  if (website) return res.status(200).json({ success: true, message: 'Thank you!' });

  if (!initiative_name || !email || !framework_text) {
    return res.status(400).json({ success: false, message: 'Please fill in all required fields.' });
  }
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!emailRegex.test(email)) {
    return res.status(400).json({ success: false, message: 'Please enter a valid email address.' });
  }
  // Capture into the leads pipeline (best-effort — never blocks the email).
  try {
    await insertLead({
      source: 'eval',
      name: initiative_name || 'Eval framework lead',
      email,
      business: null,
      summary: use_case ? 'Use case: ' + use_case : 'Eval framework',
      payload: { initiative: initiative_name, use_case, context, sensitivity },
    });
  } catch (e) { console.error('lead capture failed:', e); }

  const reportHtml = buildFrameworkHtml({ initiative_name, use_case, framework_text });

  const leadBody = [
    'New Eval Framework Builder submission.',
    '',
    `Initiative:   ${initiative_name}`,
    `From:         ${email}`,
    `Use case:     ${use_case || '—'}`,
    `Context:      ${context || '—'}`,
    `Sensitivity:  ${sensitivity || '—'}`,
    '',
    '─────────────────────────────',
    'Framework sent to the user:',
    '',
    framework_text,
  ].join('\n');

  const ownerHtml = ownerLeadEmail({
    source: 'Eval Framework', name: initiative_name, replyEmail: email,
    rows: [
      ['Email', email], ['Initiative', initiative_name], ['Use case', use_case],
      ['Context', context], ['Sensitivity', sensitivity],
    ],
  });

  try {
    const [userRes, leadRes] = await Promise.allSettled([
      sendEmail({
        from: FROM,
        to: email,
        reply_to: REPLY_TO,
        subject: `Your eval framework for ${initiative_name}`,
        html: reportHtml,
        text: framework_text,
      }),
      sendEmail({
        from: FROM,
        to: OWNER,
        reply_to: email,
        subject: `New eval framework lead — ${initiative_name} (${use_case || 'framework'})`,
        html: ownerHtml,
        text: leadBody,
      }),
    ]);

    const userOk = userRes.status === 'fulfilled' && userRes.value.ok;
    if (!userOk) {
      if (userRes.status === 'fulfilled') {
        const err = await userRes.value.json().catch(() => ({}));
        console.error('Resend error (user framework):', err);
      } else {
        console.error('Fetch error (user framework):', userRes.reason);
      }
      return res.status(500).json({ success: false, message: 'Something went wrong — please try again.' });
    }
    if (leadRes.status !== 'fulfilled' || !leadRes.value.ok) {
      console.error('Resend warning (eval lead notification did not send).');
    }

    return res.status(200).json({ success: true, message: `Framework sent to ${email}. Check your inbox.` });
  } catch (err) {
    console.error('Eval send error:', err);
    return res.status(500).json({ success: false, message: 'Something went wrong — please try again.' });
  }
}
